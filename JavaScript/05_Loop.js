// for 문
for (let i = 0; i < 5; i++) {
    console.log(i);
}

// for - in 문
const arr = [1, 2, 3, 4, 5];
for (let i in arr) {
    console.log(i);
}

// for - of 문
const arr2 = [3, 5, 1, 7];
for (let i of arr2) {
    console.log(i);
}

// while문
let i = 0;
while (i < 5) {
    console.log(i);
    i++;
}

// do - while 문
let j = 0;
do {
    console.log(j);
    j++;
} while (j < 5);
