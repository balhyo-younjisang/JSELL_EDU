// if문
let a = 10, b = 5;

if (a > b) {
    console.log(`${a}가 ${b}보다 큽니다`);
}
// if - else 문
let c = 10, d = 5;

if (c > d) {
    console.log(`${c}가 ${d}보다 큽니다`);
} else {
    console.log(`${c}가 ${d}보다 작습니다`);
}

// else - if 문
let e = 10, f = 5;

if (e > f) {
    console.log(`${e}가 ${f}보다 큽니다`);
} else if (e < f) {
    console.log(`${e}가 ${f}보다 작습니다`);
} else {
    console.log(`${e}와 ${f}는 같습니다`);
}